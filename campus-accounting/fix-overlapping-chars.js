// 要修复的文件类型
const targetExtensions = ['.js', '.wxml', '.wxss', '.json', '.md'];

// 遍历目录并修复重叠字符
function fixFiles(dir) {
  const files = fs.readdirSync(dir);
  
  files.forEach(file => {
    const filePath = path.join(dir, file);
    const stat = fs.statSync(filePath);
    
    if (stat.isDirectory()) {
      // 跳过 node_modules 和 .git 目录
      if (file === 'node_modules' || file === '.git') {
        return;
      }
      fixFiles(filePath);
    } else if (stat.isFile()) {
      // 检查文件类型
      const ext = path.extname(file).toLowerCase();
      if (targetExtensions.includes(ext)) {
        try {
          fixOverlappingChars(filePath);
        } catch (error) {
          console.error(`修复文件失败: ${filePath}`, error);
        }
      }
    }
  });
}

// 修复单个文件中的重叠字符
function fixOverlappingChars(filePath) {
  let content = fs.readFileSync(filePath, 'utf8');
  let fixed = false;
  
  // 替换所有重叠字符
  Object.keys(overlappingChars).forEach(oldChar => {
    const newChar = overlappingChars[oldChar];
    const regex = new RegExp(oldChar, 'g');
    if (content.includes(oldChar)) {
      content = content.replace(regex, newChar);
      fixed = true;
      console.log(`文件 ${filePath} 中已替换 "${oldChar}" 为 "${newChar}"`);
    }
  });
  
  // 如果文件内容有修改，保存到文件
  if (fixed) {
    fs.writeFileSync(filePath, content, 'utf8');
  }
}

// 执行修复
console.log('开始修复项目中的重叠字符问题...');
fixFiles(projectDir);
console.log('修复完成！');
